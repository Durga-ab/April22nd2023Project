package mathematicsInJava;

public class UnaryOperatorPrefix 
{//(++expression)
 public static void main(String[]args)
 {
	int a = 250;
	System.out.println(a);//250
	System.out.println(++a);//251
	System.out.println(++a);//252
	System.out.println(a);//252
	System.out.println(++a);//253
	System.out.println(++a);//254
	System.out.println(a);//254
	
 }
}
