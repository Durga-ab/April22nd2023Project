package mathematicsInJava;

public class UnaryOperator 
{
	public static void main (String[]args)
	{
		int a = 12;
		System.out.println(+a);//12
		System.out.println(a);//12
		System.out.println(+a);//12
		
		int b = -12;
		System.out.println(b);//-12
		System.out.println(-b);//12
		System.out.println(b);//-12
		System.out.println(-b);//12
		
	}
 
 
}
